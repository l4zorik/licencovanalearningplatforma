export const getCertificationsByCategory = (category: CertificationCategory): Certification[] => {
  return CERTIFICATION_DATA.filter(cert => cert.category === category);
};

export const getCertificationById = (id: string): Certification | undefined => {
  return CERTIFICATION_DATA.find(cert => cert.id === id);
};

export const searchCertifications = (query: string): Certification[] => {
  const lowerQuery = query.toLowerCase();
  return CERTIFICATION_DATA.filter(cert =>
    cert.name.toLowerCase().includes(lowerQuery) ||
    cert.provider.toLowerCase().includes(lowerQuery) ||
    (cert.category || '').toLowerCase().includes(lowerQuery) ||
    cert.description.toLowerCase().includes(lowerQuery)
  );
};

export const getTrendingCertifications = (limit: number = 12): Certification[] => {
  return [...CERTIFICATION_DATA]
    .sort((a, b) => b.careerValue - a.careerValue)
    .slice(0, limit);
};
